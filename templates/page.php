        modalités et les conditions de mise à disposition des services de coworking proposés par l’association Happy Hours.
        </p>

        <h3>2. Conditions d’accès initiales</h3>
        <p>
        Tout accès et/ou utilisation des services de coworking proposés par l’association Happy Hours suppose :
        </p>
        <ul>
          <li>l’adhésion à l’association Happy Hours, pour la personne physique ou morale représentée, avec cotisation à jour ;</li>
          <li>le respect de l'ensemble des termes des présentes CGU et son acceptation inconditionnelle.</li>
        </ul>
        <p>
        Les CGU, paraphées et signées, constituent un contrat entre Happy Hours et l'usager, devenant ainsi un «coworker». 
