        <h3>3. Le cadre associatif</h3>
        <p>
        Cette prestation est proposée par l’association Happy Hours. Toute personne désirant avoir accès aux prestations proposées par l’association doit en être membre, et à jour de sa cotisation.           
        L’association Happy Hours, créée en septembre 2014, a pour objet principal de gérer et de développer un espace de coworking, espace de travail partagé et créateur d’un lien social favorisant l’échange et l’ouverture entre ses membres.
        <br>
        L’association s’appuie sur les principes de mutualisation des connaissances et moyens materiels et immateriels, en reposant sur le partage, l’entraide et la coopération entre les membres. 
        </p>
        <p>
        Le choix du cadre associatif a été fait afin de placer l’ensemble des coworkers dans les mêmes conditions. L’ensemble des coworkers est soumis à ces règles générales d’utilisation, sans exception, membres du conseil d’administration compris.
        </p>
        <p>
        L’objectif final est de rendre l’association totalement indépendante de ses fondateurs. 
        </p>
        <p>
        Il est demandé aux usagers de respecter ce choix en participant à la vie de l’association.  
        </p>

        <h3>4. Activités dans l’espace coworking</h3>
        <p>L’espace de coworking Happy Hours n’a pas vocation à :</p>
        <ul>
          <li>devenir un booster/accélérateur d’entreprises ;</li>
          <li>devenir une agence ou fonctionner comme tel.</li>
        </ul>
        <p>
        Toutes les activités réalisées par les coworkers sont totalement indépendantes de l’association Happy Hours et doivent le rester. 
        </p>
        <p>
        Les collaborations entre membres de l’espace sont encouragées mais ne sont en aucun cas obligatoires. Celles-ci ne doivent cependant en aucun cas perturber ou gêner les autres membres non-concernés par cette collaboration.  
        </p>

        <h3>5. Type de formules proposées par l’association</h3>
        <p>L’association propose deux types de formules :</p>
        <ul>
          <li><strong>Nomade</strong> - attribution d’un espace de travail individuel à la  journée ou par carnet de 10 journées ; </li>
          <li><strong>Résident</strong> - attribution d’un espace de travail individuel réservé. Prestation au mois.</li>
        </ul>
        <p>Une journée d’essai est offerte pour toute personne intéressée à devenir membre.</p>
        <p>L’espace de coworking est ouvert entre 9h00 et 19h00 du lundi au vendredi. Ces horaires sont maléables en fonction des coworkers, bien que sans garantie.</p>
        <p>Les prix de références sont ceux disponibles sur le site internet. Les tarifs ne sont modifiables que par décision du conseil d’administration de l’association Happy Hours.</p>

        <h3>6. Conditions de paiement et facturation</h3>
        <p><strong>Résident</strong> : Les paiements sont dûs d’avance le premier de chaque mois. A terme, il est demandé de mettre en place un virement automatique.</p>
        <p><strong>Nomade</strong> : Les journées de coworking sont payable via l’achat préalable de tickets.</p>
        <p>L’association accepte deux types de paiement : chèque ou virement, et s’engage à fournir toute facture nécessaire à l’usager.</p>

        <h3>7. Cas du «résident» : Engagement initial et rupture de contrat</h3>
        <p>L’engagement initial pour un «résident» est de 2 mois.</p>
        <p>Toute rupture de contrat est réalisable par l’usager ou par l’association avec un préavis d’un mois, par écrit (e-mail ou lettre). La dernière facture est réalisée au prorata du nombre de jours passés dans l’espace de coworking.</p>

        <h3>8. Mise à disposition d’un espace de travail</h3>
        <p>L’association Happy Hours s’engage à mettre à disposition du coworker un espace de travail individuel en fonction des espaces de travail vacants suivant la date à laquelle l’usager se présente.</p>
        
        <h3>9. Attribution d’un espace de travail</h3>
        <p>Seuls les membres «résidents» ont un espace de travail individuel réservé, à l’emplacement défini à la date de début du contrat ou lors de la mise en place d’une disposition et/ou répartition des bureaux.</p>
        <p>Le bureau n’est cependant ni nominatif ni attribué à durée illimitée. L’attribution et l’emplacement des coworkers dans l’espace de travail peut être modifié à tout moment, avec communication générale au préalable.</p>
        <p>L’accès aux espaces de travail pour les utilisateurs «nomades» se fait dans les limites des places disponibles. Sauf réservation au préalable, ces places sont allouées par ordre d’arrivée lors de la journée.</p>

        <h3>10. Clés et sécurité</h3>
        <p>Pour des raisons de sécurité, l’objectif de l’association est de limiter le nombre de jeux de clés en usage. Par défaut, les jeux de clés disponibles sont distribués entre les membres du conseil d’administration.</p>
        <p>Un jeu de clé «volant» est cependant mis à disposition des usagers sous réserve d’un pointage lors de leur emprunt.</p>
        <p>L’usager ne doit en aucun cas en faire des copies ou permettre à quiconque de les utiliser. Toute perte doit être notifiée immédiatement et il sera demandé de payer le coût de remplacement des clés ou le remplacement des serrures si nécessaire.</p>
        
        <h3>11. État et respect de l’espace de travail</h3>
        <p>L’usager devra prendre soin des espaces de travail collectifs et individuels, de la salle de réunion, des espaces communs et de leurs équipements, installations et des meubles.</p>
        <p>L’usager est responsable de tout dommage qu’il causerait aux espaces de travail collectifs et individuels, à la salle de réunions, aux espaces communs et à leurs équipements, installations et meubles, ainsi qu’au matériel des autres coworkers.</p>
        
        <h3>12. Titulaire du service</h3>
        <p>Le présent Contrat ne permet, à titre exclusif, l’accès à l’espace de travail réservé qu’à la seule personne signataire du présent Contrat, sans que l’accès à l’espace de travail ne puisse être cédé, vendu, prêté ou consenti sous quelque forme que ce soit.</p>
        
        <h3>13. La nature d’activité des membres</h3>
        <p>Les locaux ne peuvent être utilisés en dehors d’un usage  administratif et de bureau.</p>
        <p>L’usage des espaces individuels de travail ou des autres salles pour de la vente au détail ou impliquant des visites fréquentes du public n’est pas autorisé. L’usager ne doit pas associer le nom de Happy Hours avec son activité, sauf par autorisation préalable de l’association Happy Hours.</p>
        <p>Il doit informer spontanément de tout changement de coordonnées (adresse postale, e-mail, téléphone), par rapport à celles figurant aux Conditions Particulières.</p>
        
        <h3>14. Salle de réunion</h3>
        <p>L’utilisation de la salle de réunion est possible pour tous les coworkers («nomades» ou «résidents»), à condition de réserver l’espace au préalable.</p>
        <p>L’utilisation de cet espace doit se faire dans un esprit de partage avec les autres coworkers. Toute utilisation intensive de la salle (plus d’une demie-journée) devra faire l’objet d’une demande spécifique.</p>
        <p>Tout abus d’utilisation de la salle de réunion par un coworker pourra être signalée et contrôlée. L’utilisation doit se faire dans un esprit «fair-use».</p>
        
        <h3>15. Réservation</h3>
        <p>Les réservations sont possibles pour les espaces de travail individuels nomades ou pour la salle de réunion, par téléphone ou contact direct auprès d’un membre du conseil d’administration. Celui-ci se charge ainsi de noter et réserver les dates demandées, dans la limite des disponibilités.
        
